package com.balikesirbitirme.otizmleyasam.Gamepage.MimicPage;

public class Mimic {
    private int intHuman;
    private int intMimic;

    public Mimic(int intHuman, int intMimic) {
        this.intHuman = intHuman;
        this.intMimic = intMimic;
    }

    public int getIntHuman() {
        return intHuman;
    }

    public void setIntHuman(int intHuman) {
        this.intHuman = intHuman;
    }

    public int getIntMimic() {
        return intMimic;
    }

    public void setIntMimic(int intMimic) {
        this.intMimic = intMimic;
    }
}
